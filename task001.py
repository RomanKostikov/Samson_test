# Требуется написать функцию, которая принимает строку и возвращает её перевёрнутый вариант.
# Затем написать программу, которая использует эту функцию с фиксированным набором строк.
# Функция для переворачивания строки
def reverse_string(s):
    return s[::-1]


# Тестовая программа
if __name__ == "__main__":
    test_strings = ['abc', 'hello', 'Python', '12345']
    for s in test_strings:
        reversed_s = reverse_string(s)
        print("Оригинал: {}\tПеревёрнутая: {}".format(s, reversed_s))
